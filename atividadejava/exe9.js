// Faça um programa onde os jurados vão dar a explicação para 
//cada nota dada. São permitidas notas de 0 a 10. Cada nota deve
// conter uma mensagem diferente.

const jurado = 0

switch (jurado) {
    case 0:
        console.log("muito baixa")

        break;

    case 1:
        console.log("muito baixa")

        break;

    case 2:
        console.log("bem baixa")

        break;
    case 3:
        console.log("ta ruim ainda")

        break;
    case 4:
        console.log("ainda pode melhorar")

        break;
    case 5:
        console.log("ta melhor")

        break;
    case 6:
        console.log("ta ficando bom")

        break;
    case 7:
        console.log("vamos tirar mais")

        break;
    case 8:
        console.log("ta quase lá")

        break;
    case 9:
        console.log("mais um pouquinho")

        break;
    case 10:
        console.log("excelente")

        break;

    default:
        break;
}